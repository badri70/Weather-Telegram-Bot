def map_pic_code_to_icon(code):
    if code == 'rain':
        return "🌧"
    elif code == 'overcast clouds':
        return "🌥"
    elif code == 'clear sky':
        return "☀️"
    elif code == 'light rain':
        return "🌦"
    elif code == 'scattered clouds':
        return "⛅️"
    else:
        return "🌤"
